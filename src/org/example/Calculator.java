package org.example;

public class Calculator {
    
    // var

    // method
    public int addition(int num1, int num2)
    {
        return num1 + num2;
    }

    public int substraction(int num1, int num2)
    {
        return num1 - num2;
    }

    public int multicplication(int num1, int num2)
    {
        return num1 * num2;
    }

    public int division(int num1, int num2)
    {
        return num1 / num2;
    }
}
